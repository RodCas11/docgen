/**
 * Soma dois números.
 * @param {number} a - Primeiro número.
 * @param {number} b - Segundo número.
 * @returns {number} - A soma de a e b.
 */
function add(a, b) {
    return a + b;
}

/**
 * Subtrai dois números.
 * @param {number} a - Primeiro número.
 * @param {number} b - Segundo número.
 * @returns {number} - A subtração de b de a.
 */
const subtract = (a, b) => {
    return a - b;
}
